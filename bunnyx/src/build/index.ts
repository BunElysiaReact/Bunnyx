// bunnyx/src/build/index.ts

import { join } from 'path';
import { existsSync, mkdirSync } from 'fs';
import type { ResolvedBunnyConfig, BunnyBuildResult } from '../types/index.ts';
import { logger } from '../utils/logger.ts';
import { buildProduction } from 'bertui/build';

const TOTAL_STEPS = 3;

export async function runBuild(config: ResolvedBunnyConfig): Promise<BunnyBuildResult> {
  logger.banner();
  const start  = Date.now();
  const root   = config.root;
  const outDir = join(root, config.build.outDir);

  try {
    // ── Step 1: BertUI production build (all 10 internal steps) ────────────
    logger.step(`[1/${TOTAL_STEPS}] Building BertUI...`);
    await buildProduction({ root });
    logger.success(`[1/${TOTAL_STEPS}] BertUI build complete`);

    // ── Step 2: Bundle Elysia server ────────────────────────────────────────
    logger.step(`[2/${TOTAL_STEPS}] Bundling Elysia server...`);
    if (existsSync(config.server)) {
      const serverOutDir = join(outDir, 'server');
      mkdirSync(serverOutDir, { recursive: true });

      const result = await Bun.build({
        entrypoints: [config.server],
        outdir:      serverOutDir,
        target:      'bun',
        format:      'esm',
        minify:      { whitespace: true, syntax: true },
        external:    ['elysia', '@elysiajs/*', 'bun', 'bun:*'],
      });

      if (!result.success) {
        throw new Error(`Server bundle failed:\n${result.logs?.map(l => (l as any).message).join('\n')}`);
      }
      logger.success(`[2/${TOTAL_STEPS}] Elysia server bundled`);
    } else {
      logger.warn(`[2/${TOTAL_STEPS}] No server file found — skipping server bundle`);
    }

    // ── Step 3: Write production launcher ───────────────────────────────────
    logger.step(`[3/${TOTAL_STEPS}] Writing production launcher...`);
    await writeProductionLauncher(outDir, config);
    logger.success(`[3/${TOTAL_STEPS}] Launcher written`);

    const duration = Date.now() - start;

    console.log(`\n  \x1b[32m\x1b[1m✓ Build complete\x1b[0m  \x1b[90m${(duration / 1000).toFixed(2)}s\x1b[0m`);
    console.log(`  \x1b[90mOutput: ${config.build.outDir}/\x1b[0m\n`);

    return { success: true, outDir, duration };

  } catch (err: any) {
    logger.error(`Build failed: ${err.message}`);
    throw err;
  }
}

async function writeProductionLauncher(outDir: string, config: ResolvedBunnyConfig) {
  const hasServer = existsSync(config.server);
  const port      = config.dev.port;

  // BertUI build output layout:
  //   dist/
  //     assets/         ← content-hashed JS + CSS bundles
  //     images/         ← copied from src/images/
  //     <route>/index.html  ← pre-rendered HTML per route
  //     index.html      ← root route
  //     sitemap.xml
  //     robots.txt

  const launcher = `#!/usr/bin/env bun
// 🐰 Bunnyx — Production Launcher (auto-generated by \`bunnyx build\`)

import { join, extname } from 'path';
import { Elysia } from 'elysia';

const PORT = parseInt(process.env.PORT ?? '${port}');
const DIST = new URL('.', import.meta.url).pathname;

const app = new Elysia();

// ── 1. Elysia API ────────────────────────────────────────────────────────────
${hasServer ? `
try {
  const mod = await import('./server/index.js');
  if (mod.default) {
    if (mod.default.server) { try { mod.default.server.stop(); } catch {} }
    app.use(mod.default);
    console.log('✓ API routes mounted');
  }
} catch (err) {
  console.warn('⚠  Could not load server:', err.message);
}` : `// No server file was present at build time`}

// ── 2. Hashed assets (JS/CSS) ────────────────────────────────────────────────
app.get('/assets/*', async ({ params, set }) => {
  const file = Bun.file(join(DIST, 'assets', params['*']));
  if (!(await file.exists())) { set.status = 404; return 'Not found'; }
  const ext = extname(params['*']).toLowerCase();
  set.headers['Content-Type']  = ext === '.css' ? 'text/css' : 'application/javascript; charset=utf-8';
  set.headers['Cache-Control'] = 'public, max-age=31536000, immutable';
  return file;
});

// ── 3. Images ────────────────────────────────────────────────────────────────
app.get('/images/*', async ({ params, set }) => {
  const file = Bun.file(join(DIST, 'images', params['*']));
  if (!(await file.exists())) { set.status = 404; return 'Not found'; }
  return file;
});

// ── 4. Sitemap + robots ──────────────────────────────────────────────────────
app.get('/sitemap.xml', async ({ set }) => {
  const file = Bun.file(join(DIST, 'sitemap.xml'));
  if (!(await file.exists())) { set.status = 404; return ''; }
  set.headers['Content-Type'] = 'application/xml';
  return file;
});
app.get('/robots.txt', async ({ set }) => {
  const file = Bun.file(join(DIST, 'robots.txt'));
  if (!(await file.exists())) { set.status = 404; return ''; }
  set.headers['Content-Type'] = 'text/plain';
  return file;
});

// ── 5. Root-level public files (favicon.svg etc.) ───────────────────────────
app.get('/*', async ({ params, set }, next) => {
  const slug = params['*'];
  if (!slug?.includes('.')) return (next as any)?.();
  const file = Bun.file(join(DIST, slug));
  if (!(await file.exists())) return (next as any)?.();
  return file;
});

// ── 6. Pre-rendered HTML pages (BertUI build output) ────────────────────────
// BertUI writes dist/<route>/index.html for every page.
// e.g. /blog/hello → dist/blog/hello/index.html
app.get('*', async ({ request, set }) => {
  const pathname    = new URL(request.url).pathname.replace(/^\\//, '').replace(/\\/$/, '');
  const routeHtml   = Bun.file(pathname ? join(DIST, pathname, 'index.html') : join(DIST, 'index.html'));
  const fallbackHtml = Bun.file(join(DIST, 'index.html'));

  const page = (await routeHtml.exists()) ? routeHtml
             : (await fallbackHtml.exists()) ? fallbackHtml
             : null;

  if (!page) { set.status = 404; return 'Not found'; }
  set.headers['Content-Type'] = 'text/html';
  return page;
});

app.listen(PORT);
console.log(\`🐰 Bunnyx running at http://localhost:\${PORT}\`);
`;

  await Bun.write(join(outDir, 'start.js'), launcher);
  await Bun.write(join(outDir, 'package.json'), JSON.stringify({
    name:    'bunny-app',
    type:    'module',
    scripts: { start: 'bun start.js' },
    dependencies: { elysia: '*' },
  }, null, 2));
}
